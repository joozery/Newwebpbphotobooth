import axios from 'axios';

// ‡∏™‡∏£‡πâ‡∏≤‡∏á axios instance
const api = axios.create({
  baseURL: import.meta.env.VITE_API_BASE_URL || 'https://pbbackend-api-4e56bf125d15.herokuapp.com',
  timeout: 30000, // 30 seconds timeout for better reliability
  headers: {
    'Content-Type': 'application/json',
  }
});

// Request interceptor
api.interceptors.request.use(
  (config) => {
    console.log('üöÄ API Request:', config.method?.toUpperCase(), config.url);
    console.log('üì° Base URL:', config.baseURL);
    console.log('üîó Full URL:', config.baseURL + config.url);
    return config;
  },
  (error) => {
    console.error('‚ùå API Request Error:', error);
    return Promise.reject(error);
  }
);

// Response interceptor
api.interceptors.response.use(
  (response) => {
    console.log('‚úÖ API Response:', response.status, response.data);
    return response;
  },
  (error) => {
    console.error('‚ùå API Error:', {
      status: error.response?.status,
      statusText: error.response?.statusText,
      data: error.response?.data,
      message: error.message,
      code: error.code,
      config: {
        url: error.config?.url,
        method: error.config?.method,
        baseURL: error.config?.baseURL
      }
    });
    
    // ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£ error ‡∏ó‡∏µ‡πà‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡πÄ‡∏à‡∏≤‡∏∞‡∏à‡∏á
    if (error.response?.status === 404) {
      console.error('üîç API endpoint not found');
    } else if (error.response?.status === 500) {
      console.error('üí• Server error');
    } else if (!error.response) {
      console.error('üåê Network error - no response from server');
    }
    
    return Promise.reject(error);
  }
);

